    function inicializarAnimacionPasos() {
        const pasos = contenidoDiv.querySelectorAll('.paso');
        
        if (pasos.length === 0) {
            return;
        }

        if (window.pasosObserver) {
            window.pasosObserver.disconnect();
        }

        const observer = new IntersectionObserver((entries) => {
            entries.forEach(entry => {
                if (entry.isIntersecting) {
                    const pasoElement = entry.target;
                    const index = Array.from(pasos).indexOf(pasoElement);
                    setTimeout(() => {
                        pasoElement.classList.add('mostrar');
                    }, index * 200);
                } else {
                    entry.target.classList.remove('mostrar');
                }
            });
        }, {
            threshold: 0.5
        });

        pasos.forEach(paso => {
            observer.observe(paso);
        });

        window.pasosObserver = observer;
    }

    function crearCarrusel(idContainer, dataArray) {
        const container = document.getElementById(idContainer);
        if (!container) return;

        let html = '<div class="owl-carousel">';
        dataArray.forEach((src, index) => {
            html += `<div><img src="${src}" alt="Imagen ${index + 1}" class="img-fluid"></div>`;
        });
        html += '</div>';

        container.innerHTML = html;

        $(`#${idContainer} .owl-carousel`).owlCarousel({
            items: 3,
            loop: true,
            autoplay: true,
            autoplayTimeout: 2500,
            dots: true
        });
    }

