            const thumbnailWidth = targetThumbnail.offsetWidth;
            const thumbnailLeft = targetThumbnail.offsetLeft;
            const centerPosition = thumbnailLeft - (wrapperWidth / 2) + (thumbnailWidth / 2);
            thumbnailsWrapperGraduados.scrollTo({
                left: centerPosition,
                behavior: 'smooth'
            });
